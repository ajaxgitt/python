# Problema de Ordenamiento y Búsqueda:
# Dado un conjunto de puntos en un plano, encuentra los dos puntos más cercanos entre sí.

# Problema de Grafos (DFS/BFS):
# Dado un grafo no dirigido y conexo, encuentra el número mínimo de aristas que se deben eliminar para desconectar el grafo en exactamente dos componentes.

# Problema de Algoritmos Geométricos:
# Dados varios segmentos de línea en un plano, encuentra todos los puntos de intersección entre los segmentos.

# Problema de Teoría de Números:
# Dado un número entero positivo n, encuentra la cantidad de formas en que n puede ser expresado como la suma de números consecutivos positivos.

# Problema de Programación Dinámica:
# Dado un conjunto de monedas con diferentes valores y una cantidad específica, encuentra el número mínimo de monedas necesarias para sumar exactamente esa cantidad.

# Problema de Flujo en Redes:
# Dado un grafo dirigido ponderado que representa una red de transporte, encuentra el flujo máximo posible desde un nodo fuente a un nodo sumidero.

# Problema de Geometría Computacional:
# Dados varios puntos en un plano, encuentra el área de la figura formada por la unión de todos los triángulos que se pueden formar utilizando estos puntos como vértices.

# Problema de Teoría de Juegos (Juego Nim):
# Dado un juego de Nim con varias pilas de piedras, determina si el jugador que empieza tiene una estrategia ganadora si ambos juegan óptimamente.

# Problema de Codificación y Decodificación:
# Dado un mensaje codificado y una clave de desplazamiento, decodifica el mensaje original.

# Problema de Grafos (Algoritmo de Floyd-Warshall):
# Dado un grafo ponderado dirigido con pesos positivos y negativos, encuentra el camino más corto entre todos los pares de nodos.