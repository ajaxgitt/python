# Ejercicio: Gestión de una Biblioteca Virtual


# Descripción del problema:
# Imagina que estás desarrollando un sistema para gestionar una biblioteca virtual. 
# La biblioteca tiene libros y usuarios registrados. Debes implementar varias funcionalidades clave:


# Clases y Objetos:

# Definir clases para representar Libro y Usuario, con atributos como título, autor, ISBN 
# para libros, y nombre, apellido, correo electrónico para usuarios.

# Manejo de Datos:

# Implementar métodos para agregar libros nuevos, registrar usuarios nuevos, y permitir a los usuarios tomar prestado o devolver libros.
# Validación y Manejo de Excepciones:

# Validar que los ISBN de los libros sean únicos.
# Implementar manejo de excepciones para situaciones como libros ya prestados, usuarios que intentan tomar prestados más libros de los permitidos, etc.
# Interacción con el Usuario:

# Desarrollar un menú interactivo en consola donde los usuarios puedan registrarse, buscar libros por título o autor, tomar prestado libros, devolver libros, y verificar el estado de sus préstamos.
# Persistencia de Datos:

# Opcionalmente, implementa la persistencia de datos utilizando archivos CSV, JSON o bases de 
# datos SQLite para almacenar información sobre libros y usuarios de manera que los datos se 
# mantengan entre ejecuciones del programa.

# Puntos a considerar:
# Utiliza programación orientada a objetos (OOP) para estructurar tu código de manera 
# modular y reutilizable.
# Implementa funciones y métodos con nombres descriptivos y comentarios para explicar el 
# propósito de cada parte del código.
# Maneja adecuadamente los errores y excepciones para que el programa sea robusto frente a 
# diferentes situaciones.
# Diseña un flujo de interacción intuitivo para los usuarios del sistema de biblioteca virtual.
# Este ejercicio te permite practicar varios aspectos de programación como OOP, manejo de datos, interacción con el usuario, manejo de excepciones y persistencia de datos. Además, te desafía a pensar de manera crítica y resolver problemas complejos de manera estructurada.






